package collectionsset;

public class Student {

    // --- attributes ---------------------------------------------------------

    private String name;
    private int height;

    // --- constructors -------------------------------------------------------

    public Student(String name, int height) {
        this.name = name;
        this.height = height;
    }

    // --- getters and setters ------------------------------------------------

    public String getName() { return name; }
    public int getHeight() { return height; }
}