package introinheritance.coffee;

public class Coffee {

    // --- attributes ---------------------------------------------------------

    private String name;
    private int price;

    // --- getters and setters ------------------------------------------------

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public int getPrice() { return price; }
    public void setPrice(int price) { this.price = price; }
}